#include <unistd.h>

int main(int ac, char **av)
{
	char *s;
	int i;

	i = 0;
	if (ac > 1)
	{
		s = av[ac - 1];
		while (s[i])
			write(1, &s[i++], 1);
	}
	write(1, "\n", 1);
}
