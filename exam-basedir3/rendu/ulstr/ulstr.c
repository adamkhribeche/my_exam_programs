#include <unistd.h>

int main(int ac, char **av)
{
	char *s;
	int i;
	char c;

	i = 0;
	if (ac == 2)
	{
		s = av[1];
		while (*s)
		{
			if (*s >= 'a' && *s <= 'z')
				c = *s - 'a' + 'A';
			else if (*s >= 'A' && *s <= 'Z')
				c = *s - 'A' + 'a';
			else 
				c = *s;
			write(1, &c, 1);
			s++;
		}
	}
	write(1, "\n", 1);
}
